const TOTAL_VERSES = Object.keys(DAILY_VERSES).length; // 775

function getDayOfYear(): number {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const diff = now.getTime() - start.getTime();
  const day = Math.floor(diff / (1000 * 60 * 60 * 24));
  return day; // 1-366
}

export async function GET(req: NextRequest) {
  const appKey = process.env.YVP_APP_KEY;
  if (!appKey) {
    return NextResponse.json({ error: "Server not configured" }, { status: 500 });
  }

  const dayOfYear = getDayOfYear();
  // Fallback: if day exceeds our map length, wrap around
  const index = ((dayOfYear - 1) % TOTAL_VERSES) + 1;
  const verseEntry = DAILY_VERSES[index];

  if (!verseEntry) {
    return NextResponse.json({ error: "No verse for today" }, { status: 404 });
  }

  try {
    const url = `https://api.youversion.com/v1/bibles/${verseEntry.bibleId}/passages/${verseEntry.ref}`;
    const res = await fetch(url, {
      headers: { "x-yvp-app-key": appKey },
      next: { revalidate: 3600 }, // cache for 1 hour
    });

    if (!res.ok) {
      return NextResponse.json({ error: "Bible API error" }, { status: res.status });
    }

    const data = await res.json();
    return NextResponse.json({
      verse: data.content,
      reference: data.reference,
      version: "BSB",
    });
  } catch (e) {
    return NextResponse.json({ error: "Failed to fetch verse" }, { status: 500 });
  }
}
